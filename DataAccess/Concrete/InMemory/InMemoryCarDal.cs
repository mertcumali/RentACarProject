        List<Car> _cars;
        public InMemoryCarDal()
        {
            _cars = new List<Car>
            {
                new Car{CarId=1,BrandId=2,ColorId=3,ModelYear="2017",DailyPrice=125,Description="Economic"},
                new Car{CarId=2,BrandId=1,ColorId=2,ModelYear="2020",DailyPrice=450,Description="Sport"},
                new Car{CarId=3,BrandId=1,ColorId=2,ModelYear="2021",DailyPrice=350,Description="HatchBack"},
                new Car{CarId=4,BrandId=2,ColorId=1,ModelYear="2019",DailyPrice=575,Description="Luxury"},
            };
        }
        public void Add(Car car)
        {
            _cars.Add(car);
        }

        public void Delete(Car car)
        {
            Car carToDelete = _cars.SingleOrDefault(p => p.CarId == car.CarId);

            _cars.Remove(carToDelete);
        }
        public void Update(Car car)
        {
            Car carToUpdate = _cars.SingleOrDefault(p => p.CarId == car.CarId);
            carToUpdate.BrandId = car.BrandId;
            carToUpdate.ColorId = car.ColorId;
            carToUpdate.DailyPrice = car.DailyPrice;
            carToUpdate.Description = car.Description;
            carToUpdate.ModelYear = car.ModelYear;
        }
        public List<Car> GetAll()
        {
            return _cars;
        }

        public List<Car> GetById(int carId)
        {
            return _cars.Where(p => p.CarId == carId).ToList();
        
        }

